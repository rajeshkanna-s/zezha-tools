function estimateOnRoad(ex: number, fuel: string, city: string): number {
    let taxPct = 0;
    const d = fuel === 'Diesel';
    const ev = fuel === 'Electric';
    if (ev) return Math.round(ex * 1.03 + 600); // EV exempt road tax most states
    if (city === 'delhi') { taxPct = ex <= 600000 ? (d?5:4) : ex <= 1000000 ? (d?8.75:7) : (d?12.5:10); }
    else if (city === 'mumbai' || city === 'pune') { taxPct = ex <= 1000000 ? (d?13:11) : ex <= 2000000 ? (d?14:12) : (d?15:13); }
    else if (city === 'bangalore') { taxPct = ex <= 500000 ? 14 : ex <= 1000000 ? 15 : ex <= 2000000 ? 17 : 18; }
    else if (city === 'chennai') { taxPct = ex <= 500000 ? 12 : ex <= 1000000 ? 13 : ex <= 2000000 ? 18 : 20; }
    else if (city === 'hyderabad') { taxPct = ex <= 500000 ? 13 : ex <= 1000000 ? 14 : ex <= 2000000 ? 17 : 18; }
    else if (city === 'kolkata') { taxPct = ex <= 800000 ? 10 : ex <= 1500000 ? 12 : 14; }
    else if (city === 'ahmedabad') { taxPct = ex <= 600000 ? 6 : ex <= 1000000 ? 8 : ex <= 2000000 ? 10 : 12; }
    if (fuel === 'CNG') taxPct = Math.max(taxPct - 2, 4);
    const roadTax = ex * (taxPct / 100);
    const insurance = ex * 0.03;
    const reg = ex > 1000000 ? 3000 : ex > 500000 ? 1500 : 600;
    const tcs = ex > 1000000 ? ex * 0.01 : 0;
    return Math.round(ex + roadTax + insurance + reg + 600 + tcs);
}

